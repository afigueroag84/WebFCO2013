            <p class="title" data-section-title><a href="#historia2"><img src="../images/programas/historias/ThumbLuisAndres.jpg"/></a></p>
            <div class="content" data-section-content>
              <h2>Luis Andrés García Bautista</h2>
              <p>Bécalos y Fundación Comunitaria Oaxaca A. C. en la UESA – LEMSC </p>
              <p>Cuando me invitan a escribir como “becario” inmediatamente me pongo a pensar en todos los logros y alcances que tienen dos instituciones 
                que hoy en día forman parte de mi vida: Bécalos y Fundación Comunitaria Oaxaca.</p>
              <p> Mi nombre es Luis Andrés, mixteco de la comunidad de Santa María Nutío, San Juan Colorado, ubicado en la Costa Chica 
                  Oaxaqueña. Estudio la Licenciatura en Educación Media Superior Comunitaria (LEMSC) que ofrece la UESA en Santa María 
                  Alotepec, comunidad Mixe incrustada en la Sierra Norte -quiero aclarar que esta licenciatura es de reciente creación y quizás por eso 
                  nunca antes habían escuchado de ella- a pesar de lo anterior tengo la oportunidad de contar con la beca poco más de 6 meses, en solo ese tiempo 
                  ha transformado mi vida rotundamente y por esa razón realizo este escrito para exponerles mi experiencia como 
                  argumento para atreverme a decir que Bécalos y Fundación Comunitaria (con todos los agentes que están tras ellos) impactan fuertemente en mi vida académica. </p>
                <center><img src="../images/programas/historias/luisandres.jpg " /></center> 
              <p>Cuando ingresé a la LEMSC estaba consciente que estaba iniciando un reto difícil a pesar de estar en un municipio pequeño, primeramente porque mi familia al ser de escasos 
                recursos económicos no podía ayudarme a estudiar una licenciatura en la ciudad, y menos  en una institución privada, así que tuve que esperar dos años después del bachillerato 
                para ingresar en la LEMSC, ahí un amigo me contó sobre el trabajo que realiza fundación Comunitaria en nuestro estado y los fines que persigue junto con Bécalos, presté atención 
                en ello y contacté al encargado del programa de becas exponiéndole mi inquietud, me ofreció información al respecto y presenté los documentos solicitados, después de un 
                lapso, me informaron sobre mi aceptación a la comunidad de becarios. </p>
              <p>Al recibir la noticia me emocioné demasiado, en breve avisé a mis padres porque ellos estaban muy preocupados sobre mi estancia en Alotepec -en ese entonces estaba en 
                solicitar prórroga porque estábamos muy mal monetariamente, en gran parte a consecuencia del sismo que arrasó la costa chica oaxaqueña el año pasado, además de las 
                cosas que pasaban en la familia- al saberlo me dijeron que por ninguna razón dejara la escuela, que si me facilitaron la beca fue por los esfuerzos y logros que he tenido mi 
                historial académico, por lo tanto, tenía que aprovecharlo al máximo. Una vez que recibí mi depósito de beca tenía tanto en mente que no podía priorizar, cada 
                día me la pasé pensando en lo afortunado que soy y sobre todo en la inversión que podía hacer con mi beca; tenía muchas opciones, claro todas importantes y necesarias. Por una 
                parte podía tenerlo guardado para ir pagando cada mensualmente mi renta y comida, sin embargo no me convencía porque estaba de por medio la necesidad y urgencia de una 
                computadora personal, recordé lo difícil que habían sido los tres primeros semestres de mi licenciatura; cada vez que pedía prestado una máquina no podía revisar a mi ritmo las 
                lecturas en electrónico o realizar mis escritos porque era poco tiempo, o a veces por las tareas que teníamos nadie podía prestarme una, así que opté por esta última; me compré 
                una laptop Ahora que la tengo es más fácil, las limitaciones que antes se presentaban ahora se ven superadas, actualmente puedo leer el mayor tiempo posible, realizo mis 
                trabajos a tiempo y hasta me sobra un poco para realizar otras actividades. </p>
              <p>Estoy seguro que sin la beca difícilmente habría adquirido mi laptop; en estos momentos pienso que para mí siguiente depósito compraré una impresora para tener impresiones a 
                bajo costo, ya que como mencioné en un principio mi situación es muy crítica o quizás cambie de parecer, pero mientras esto sucede, les comento que en breve esto ha sido mi 
                vida en relación a la beca, estoy y estaré por siempre agradecido con Fundación Comunitaria, con Bécalos, con las instituciones involucradas, pero sobre todo, con 
                personas que hacen posible todo esto: los que colaboran en cada una de las dependencias, las que de alguna manera intervienen en el proceso y a quienes nos hacen 
                nos hacen llegar su donativo. </p>
              <p>¡¡¡MIL GRACIAS!!! </p>
          </section>
