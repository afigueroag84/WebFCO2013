              <h2>Silvia Citlalli López Pérez </h2>
              <p>Nací en la comunidad de San Pedro el Alto, Zimatlán de Álvarez, Oaxaca. Soy la mayor de tres hermanas y tres hermanos. Actualmente soy 
                estudiante de la Licenciatura en Médico Cirujano, en la Universidad Regional del Sureste (URSE). 
                Crecí en una familia común de comunidad, en donde había situaciones de maltrato y violencia, mismas que crearon en mí las ganas y deseos de superación, 
                para posteriormente poder ayudar a las demás personas, principalmente mi mamá.  </p>
              <p> Inicié mis estudios a la edad de 4 años en el Centro de Educación Preescolar de mi comunidad, para posteriormente ingresar a la Primaria 
                  a la Edad de 7 años, durante esta etapa de mi vida, me destaqué en concursos de conocimiento y oratoria. 
                  Posteriormente ingreso a la telesecundaria con una muy destacada participación en diferentes eventos, logrando un promedio general de 9.6, haciéndome 
                  acreedora así a una beca para continuar con mis estudios de nivel bachillerato, etapa en la que mi vida toma nuevos caminos.</p>
              <center><img src="../images/programas/historias/silvia.jpg" width="72%"/></center>
              <p>El hecho de salir de mi comunidad para ir a estudiar a la ciudad de Oaxaca, me permitió y aclarar muchísimas cosas que tenía en mente anteriormente, 
                  detrás de esas montañas supe que había mejores oportunidades de vida para mí, y la oportunidad de empezar a hacer cosas por las y los míos se 
                  estaba dando. El hecho de ver mujeres empoderadas que tomaban decisiones sobre su vida, con la responsabilidad que cada acción amerita, me inspiraban e hicieron que 
                  cada día de mi vida fuera una lucha constante por lograr aquellos sueños que de niña tenía. </p>
              <center><img src="../images/programas/historias/silviaBecalos.jpg"  width="72%"/></center>
              <p> El cambio que veía en mi persona a partir de esos momentos me gustaba mucho y anhelaba que muchas y muchos otros pudieran también beneficiarse de ello, por lo que 
                  empecé a acercarme con el médico, el amigo, la amiga, pero principalmente con mi familia, pues para poder beneficiar y ayudar a las y los demás, tenía que resolver algunos 
                  problemas de mi familia, como la violencia que mi madre sufría por parte de mi papá, y efectivamente así lo hice. Mi padre empezó a entender muchas cosas y tuve su apoyo al 
                  igual que toda mi familia para realizar actividades en mi comunidad como pláticas con los conocimientos que había y estaba adquiriendo. </p>
              <p>Lo anterior me permitió acercarme más a la comunidad, involucrar a las y los jóvenes en las diferentes actividades, es decir, involucrar a todos los actores en este cambio y 
                  acciones que quería realizar en beneficio de la comunidad. Seguí además participando en concursos de oratoria aunque en esta ocasión con temas de sensibilización hacia los 
                  derechos de las mujeres, en instancias como el Instituto de la Mujer Oaxaqueña (IMO), en el H. Congreso del Estado de Oaxaca LXI Legislatura, obteniendo muy buenos 
                  resultados lo que incluye un primer lugar. </p>
              <p> Estas y más participaciones me dieron la oportunidad para postularme como Premio Nacional de la Juventud 2011, el cual fue aprobado en el mes 
                de diciembre de 2011. A pesar de ser Premio Nacional de la Juventud, sabía, que esto no terminaba aquí, que mi futuro lo veía reflejado más allá de lo que hasta el 
                momento había conseguido, Para eso necesitaba una preparación profesional. Es por eso que mi inquietud me llevo a entrar a la Universidad estudiando la Licenciatura en Médico Cirujano, 
                carrera con la cual me siento muy contenta, actualmente me encuentro culminando el 2° semestre. Debido a problemas económicos y a los deseos de continuar preparando, 
                me atreví a solicitar la beca que ofrece la Fundación Oaxaca A.C. y el Programa Bécalos, la cual me fue otorgada en el año 2012 y con la cual e estado solventando los gastos 
                de la escuela. Gracias a esta beca tengo la oportunidad de continuar con mis estudios y a seguir adelante con mi sueño de tener una mejor preparación en el ámbito profesional, 
                ya que en un futuro mi sueño es ayudar a la gente que ahora me ofrece su apoyo y que me ha ayudado a salir adelante. </p>
              <p>Muchas gracias a la Fundación Comunitaria Oaxaca A.C., y al Programa Bécalos, por darme la oportunidad de hacer este sueño realidad. ¡Muchas Gracias! </p>
