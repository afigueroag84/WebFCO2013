            <p class="title" data-section-title><a href="#historia1" id="PaulaMiguel"><img src="../images/programas/historias/ThumbPaula.jpg" /></a></p>
            <div class="content" data-section-content>
              <h2>Paula Miguel López</h2>
              <p>Soy Paula Miguel López  estudiante de Derecho en la Universidad la Salle Oaxaca, beneficiaria del 
              <b>“Programa de becas de Fundación Comunitaria Oaxaca A. C.”</b> soy de la comunidad de Santa María Quiegolani Yautepec Oaxaca; 
                soy la más pequeña en una familia  de nueve hermanos, de los cuales ninguno tuvo la oportunidad de seguir estudiando,  
                debido a que mi madre no contaba con los recursos para darnos estudios, teniendo quecumplir el papel de la madre y el 
                padre al mismo tiempo, ya que desafortunadamente,  mi padre falleció cuando que yo tenía 3 años de edad. </p>
              <center><img src="../images/programas/historias/HistoriaPaulaMiguelLopez.jpg "  /></center><br> 
              <p>Del año <b>2002- 2004</b> estudié la Telesecundaria y de ahí seguí con el Bachillerato, al cual ingresé con muchos problemas, 
                pues, por lo regular, en las familias de las comunidades se cree que las mujeres no servimos para estudiar,  y mi madre,  quien sostenía la familia, 
                tenía esa idea, pero afortunadamente tuve la oportunidad de solicitar una beca, la cual me ayudó mucho para terminar mis estudios de nivel  
                Bachillerato en la comunidad de Santa María Quiegolani.</p>
              <p>Durante años estuve haciendo diferentes actividades en mi comunidad, participando como “Joven por el servicio” en el
                 Bachillerato Asunción Ixtaltepec, los primeros dos meses estuve trabajando en  “Grupo Integrado Marista” (GIM), que ayuda a 
                 niños con discapacidad,  además de esa comisión me asignaron la promoción y rescate de la cultura zapoteca  en la comunidad, esto lo realice hasta el mes de julio 2009.
                 En octubre de 2009,  el<a href="http://www.gesmujer.org/web/" target="_blank"> Gesmujer</a> se puso en contacto conmigo para pertenecer a un proyecto que se llamaba 
                  "Movil.com", en donde recibíamos  a mujeres líderes de cada comunidad  para un seminario de talleres, que se realizaban durante una semana de cada mes en la ciudad de Oaxaca y de 
                  ahí regresábamos a cada una a su comunidad para capacitar a un grupo de jóvenes que denominábamos “aliados”.</p>
              <p>Posteriormente, al no poder ingresar a estudiar derecho en la UABJO, por  quedar fuera de la convocatoria, unos maestros me animaron a presentar 
                el examen en la universidad la Salle Oaxaca. Actualmente curso el cuarto semestre de mi Licenciatura. Sigo luchando para salir adelante, y mi sueño más grande 
                es terminar mi carrera, porque la veo como una herramienta indispensable para poder cumplir mi meta: como mujer indígena,  apoyar a las personas de comunidades indígenas en
                donde no se tiene los recursos suficientes para contratar diferentes servicios, en las cuales también hay muchas injusticias para los habitantes y
                en especial quisiera  ayudar a mujeres de bajos recursos, por lo que me motiva seguir adelante y espero en Dios poder cumplir mi meta y ser una de 
                las mejores en el campo del Derecho.</p>
                <p>Hoy me doy cuenta que soy una mujer muy afortunada porque, a pesar de las dificultades que me he enfrentado, he 
                  salido adelante, con mucho esfuerzo pero con ganas de seguir adelante, y estoy muy agradecida con la vida y con las personas que me han ayudado
                  y que me ayudan a seguir cumpliendo mis sueños, y les agradezco a las personas que estén leyendo esta esta historia que es una más de tantas que hay
                  y que no se han escrito, yo sé que después podré contarles más sobre mis triunfos o caídas pero que me harán aprender día con día y ser fuerte para 
                  luchar por mis sueños y ayudar a los demás y así contribuir a formar una sociedad más justa y no solo ser un medio sino el fin mismo.</p>
                <p>Fundación Comunitaria Oaxaca A.C, me otorgó una  beca con la  cual me apoyo en los gastos  que  me  generan mis estudios, 
                  por ello, ahora digo muy orgullosa: <b>"soy Paula Miguel López estudiante de Derecho en la Universidad  la Salle Oaxaca, una mujer con muchas ganas de superarse y 
                  con ganas de llegar al éxito"</b>.</p>
