        <h4>Artesanos de Pinotepa de Don Luis, Sociedad de Responsabilidad Limitada Microindustrial Artesanal</h4>
<!-- <center><img src="images/programas/organizacionesred/casadebarrologo.jpg" /></center> -->
        <p> Es una organización formada por mujeres y hombres indígenas que ponen de manifiesto la riqueza cultural ancestral de su 
          comunidad a través del grabado en jícara, metal, madera y linóleo y la elaboración de textiles, tejidos en telar de 
          cintura la cual es una técnica milenaria que han heredado de sus antepasados y que con el correr del tiempo este trabajo cultural 
          se ha enriquecido con el empleo de otras técnicas y materiales.</p>
        <p>En las pinturas y grabados plasman la iconografía en donde ponen de manifiesto la influencia de su entorno al dibujar 
          flora y fauna de la región así como los animales del mar y otros continentes. Los diseños son únicos, dependiendo del tamaño de la 
          pieza y el material empleado es el tiempo que se tardan en su elaboración.</p>
